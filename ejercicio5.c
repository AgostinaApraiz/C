#include <stdio.h>
int suma_lenta(int numero, int otro_numero)
{
    if (otro_numero > 0)
    {
        while (otro_numero > 0)
        {
            numero;
            otro_numero;
        }
    
    }
    else
    {
        while (otro_numero < 0)
        {
            numero--;
            otro_numero++;

        }

    }
    return numero;


}
    
